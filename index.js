const createElement = (arr) => {
  const htmlElement = arr.map((el) => `<span class = "btn">${el}</span>`);
  return htmlElement.join(" ");
};

const manageSpinner = (status) => {
  if (status === true) {
    document.getElementById("spinner").classList.remove("hidden");
    document.getElementById("word-container").classList.add("hidden");
  } else {
    document.getElementById("word-container").classList.remove("hidden");
    document.getElementById("spinner").classList.add("hidden");
  }
};

function pronounceWord(word) {
  const utterance = new SpeechSynthesisUtterance(word);
  utterance.lang = "en-EN"; // English
  window.speechSynthesis.speak(utterance);
}

const loadLessons = () => {
  fetch("https://openapi.programming-hero.com/api/levels/all") // promise of response
    .then((response) => response.json()) // promise of json data
    .then((json) => displayLessons(json.data));
};
const removeActive = () => {
  const lessonButtons = document.querySelectorAll(".lesson-btn");
  // console.log(lessonBtn);
  lessonButtons.forEach((btn) => btn.classList.remove("active"));
};
const loadLevelWord = (id) => {
  manageSpinner(true);
  const url = `https://openapi.programming-hero.com/api/level/${id}`;
  fetch(url)
    .then((response) => response.json())
    .then((data) => {
      removeActive();
      const clickBtn = document.getElementById(`lesson-btn-${id}`);
      clickBtn.classList.add("active");
      displayLevelWord(data.data);
    });
};
const loadWordDetail = async (id) => {
  const url = `https://openapi.programming-hero.com/api/word/${id}`;
  const response = await fetch(url);
  const details = await response.json();
  displayWordDetails(details.data);
};

const displayWordDetails = (word) => {
  console.log(word);
  const detailsBox = document.getElementById("details-container");
  detailsBox.innerHTML = `
            <div>
              <h2 class="text-2xl font-bold">
                ${word.word} (<i class="fa-solid fa-microphone-lines"></i>  :${word.pronunciation})
              </h2>
            </div>
            <div>
              <h2 class="text-lg font-semibold">Meaning</h2>
              <p class="text-lg font-medium">${word.meaning}</p>
            </div>
            <div>
              <h2 class="text-lg font-semibold">Examples</h2>
              <p class="text-lg font-medium">${word.sentence}</p>
            </div>
            <div>
              <h2 class="text-lg font-semibold">Synonyms</h2>
              <div>${createElement(word.synonyms)}</div>
            </div>
  `;
  document.getElementById("word_modal").showModal();
};
const displayLevelWord = (words) => {
  // 1.get the container and empty
  const wordContainer = document.getElementById("word-container");
  wordContainer.innerHTML = "";
  if (words.length === 0) {
    wordContainer.innerHTML = `<div class="col-span-full text-center px-10 py-8 space-y-4">
           <img class="mx-auto" src="./assets/alert-error.png" alt="" />
          <p class="text-lg font-medium text-gray-400 font-bangla">
            এই Lesson এ এখনো কোন Vocabulary যুক্ত করা হয়নি।
          </p>
          <h2 class="text-2xl font-semibold font-bangla">
            নেক্সট Lesson এ যান
          </h2>
        </div>`;
    manageSpinner(false);
    return;
  }
  // 2. get into all words --> do this for of loop or for.Each()
  for (let word of words) {
    // 3.create element
    const card = document.createElement("div");
    card.innerHTML = `
     <div
          class="bg-white text-center py-10 px-6 rounded-md shadow-sm space-y-4"
        >
          <h2 class="text-2xl font-bold">${word.word ? word.word : "শব্দ পাওয়া যায়নি"}</h2>
          <p class="font-semibold">Meaning /Pronunciation</p>
          <h2 class="font-bangla font-meduim text-2xl">${word.meaning ? word.meaning : "অর্থ পাওয়া যায়নি"} /${word.pronunciation ? word.pronunciation : "pronunciation পাওয়া যায়নি"}</h2>
          <div class="flex justify-between items-center">
            <button onclick="loadWordDetail(${word.id})" class="btn bg-[#1A91FF10] hover:bg-[#1A91FF70]">
              <i class="fa-solid fa-circle-info"></i>
            </button>
            <button onclick="pronounceWord('${word.word}')" class="btn bg-[#1A91FF10] hover:bg-[#1A91FF70]">
              <i class="fa-solid fa-volume-high"></i>
            </button>
          </div>
        </div>
    `;
    wordContainer.append(card);

    // 4. append
  }
  manageSpinner(false);
};

const displayLessons = (lessons) => {
  // 1. get the container and emty
  const levelContainer = document.getElementById("level-container");
  levelContainer.innerHTML = "";
  // 2. get into all levels --> do this for of loop  or for.Each
  lessons.forEach((lesson) => {
    // 3. create element
    const btnDiv = document.createElement("div");
    btnDiv.innerHTML = `
        <button id="lesson-btn-${lesson.level_no}" onclick ="loadLevelWord(${lesson.level_no})" class="btn btn-outline btn-primary lesson-btn"><i class="fa-solid fa-book"></i> Lesson-${lesson.level_no}</button>
    `;
    // 4. append into container
    levelContainer.append(btnDiv);
  });
};
loadLessons();

document.getElementById("btn-search").addEventListener("click", () => {
  removeActive();
  const input = document.getElementById("input-search");
  const searchValue = input.value.trim().toLowerCase();
  console.log(searchValue);

  fetch("https://openapi.programming-hero.com/api/words/all")
    .then((response) => response.json())
    .then((data) => {
      const allWords = data.data;
      console.log(allWords);
      const filterWords = allWords.filter((word) =>
        word.word.toLowerCase().includes(searchValue),
      );
      displayLevelWord(filterWords);
    });
});
